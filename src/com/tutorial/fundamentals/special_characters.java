package com.tutorial.fundamentals;

public class special_characters {
    public static void main(String[] args) {
        String name = "Robert";
        System.out.println("New line: \n" + name);
        System.out.println("Tab: \t" + name);
        System.out.println("back space: \b\b" + name);
        System.out.println("Single quote: \' " + name + "\'");
        System.out.println("Double quotes: \" " + name + "\"");
    }
}
